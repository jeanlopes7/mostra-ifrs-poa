<h4 align="center">Fluxo de Trabalho:</h4>
  <ol>
    <li class="li_autor">
      O autor principal (primeiro autor) deve inscrever o trabalho no sistema.
      O trabalho ficará salvo no banco de dados do sistema e permite sua 
      edição/modificação (pelo autor principal), 
      bem como a adição de coautores e orientadores, 
      enquanto estiver aberto o prazo de inscrição de trabalhos (vide cronograma do evento).
    </li>
    <li class="li_autor">
      Todos os coautores e orientadores devem efetuar o cadastro no sistema para poderem ser vinculados ao trabalho.
    </li>
    <li class="li_autor">
      Após os coautores e orientadores efetuarem o cadastro no sistema, o autor principal deverá vincular todos eles (coautores e orientadores) ao trabalho.
    </li>
    <li class="li_autor">
      Quando o autor principal considerar que todos os dados do trabalho 
      estão completos, bem como todos coautores e orientadores foram vinculados ao trabalho,
      este (autor principal) deve entrar no sistema e executar a função ENVIAR TRABALHO.
      Após executar a função ENVIAR TRABALHO não será possível efetuar modificações no trabalho.
    </li>
    <li class="li_autor">
      Validação pelo Orientador
      <ul>
        <li>
          Após o trabalho ter sido ENVIADO pelo autor principal, o orientador do trabalho (primeiro orientador), 
          deverá entrar no sistema e efetuar a VALIDAÇÃO do trabalho, de forma a confirmar que de fato ele (primeiro orientador) 
          é o orientador do trabalho bem como garantir que todos os coautores são estudantes que participaram do trabalho.
        </li>
        <li class="li_autor">
          Caso o orientador considere que o autor deva efetuar modificações no trabalho, 
          ele (orientador) poderá colocar o trabalho de volta no estado PENDENTE,
          desde que o evento ainda esteja no prazo de inscrição de trabalhos e haja 
          tempo hábil para o autor efetuar as modificações.
        </li>
        <li class="li_autor">
          Caso o orientador considere que o trabalho não é de sua orientação, este deverá INVALIDAR o trabalho.
        </li>
      </ul>
    </li>
    <li class="li_autor">
      ATENÇÃO: apenas trabalhos VALIDADOS serão analisados pela comissão organizadora do evento.
    </li>
  </ol>
